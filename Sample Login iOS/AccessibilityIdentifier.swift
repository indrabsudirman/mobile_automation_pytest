import Foundation

enum AccessibilityIdentifier {
    enum Login {
        static let welcome = "login.welcome"
        static let title = "login.title"
        static let emailField = "login.email"
        static let passwordField = "login.password"
        static let loginButton = "login.button"
        static let messageText = "login.message"
    }
    
    enum Home {
        static let home = "home.title"
        static let message = "home.message"
    }
}
